
c=[3 8 2 10 3;8 7 2 9 7;6 4 2 7 5; 8 4 2 3 5;9 10 6 9 10];
c=c(:); %把矩阵c转化为向量
a=zeros(10,25);
for i=1:5 %实现循环运算
    a(i,(i-1)*5+1:5*i)=1;  %第i行的第i-1)*5+1个加到5*i个之和等于1
    a(5+i,i:5:25)=1;      %所有列之和等于1
end  %循环后把指派问题换为lp问题
b=ones(10,1);
[x,y]=linprog(c,[],[],a,b,zeros(25,1),ones(25,1));
X=reshape(x,5,5)
opt=y